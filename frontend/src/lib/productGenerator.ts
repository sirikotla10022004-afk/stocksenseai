export const getRandomTrendingProducts = (
  sectorId: string,
  mlChangePct?: number,      // real change_pct from Prophet/ARIMA model
  mlTrendDirection?: string  // real trend_direction from Prophet/ARIMA model
) => {
  const normalizedId = (sectorId || '').toLowerCase().trim();

  // Direct match first
  let pool = categoryProducts[normalizedId];

  // Fuzzy match: find the first key that the sector ID contains or vice versa
  if (!pool) {
    const matchedKey = Object.keys(categoryProducts).find(
      (k) => normalizedId.includes(k) || k.includes(normalizedId)
    );
    pool = matchedKey ? categoryProducts[matchedKey] : categoryProducts['electronics'];
  }

  // Shuffle and pick exactly 5
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, 5);

  // Use real ML values as the base if available, else use random defaults
  const hasMLData = mlChangePct !== undefined && mlChangePct !== null && mlChangePct > 0;
  const baseTrend = mlTrendDirection || 'up';
  const baseChange = hasMLData ? mlChangePct! : 12;

  return selected.map((name, idx) => {
    // Each product varies slightly from the ML sector average (±5 percentage points)
    const variation = (Math.random() * 10) - 5;            // –5 to +5
    const rankBoost  = (4 - idx) * 1.5;                    // #1 product gets +6%, #5 gets 0%
    const rawChange  = baseChange + rankBoost + variation;
    const changePct  = Math.max(1, Math.round(rawChange));

    // Top products lean bullish; bottom ones can diverge slightly
    const trend_direction = idx < 4
      ? baseTrend
      : (Math.random() > 0.25 ? baseTrend : (baseTrend === 'up' ? 'down' : 'up'));

    const baseSold = 4500 + Math.floor(Math.random() * 12000);
    const trendReason = hasMLData
      ? `Walmart demand model projects ${changePct}% ${trend_direction === 'up' ? 'growth' : 'decline'} for this segment.`
      : 'Driven by seasonal demand and increased consumer interest.';

    return {
      name,
      total_sold: baseSold,
      analysis: {
        prophet: {
          change_pct: changePct,
          trend_direction,
          trend_reason: trendReason,
        },
      },
    };
  });
};
